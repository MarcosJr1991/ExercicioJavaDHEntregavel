package Calculadora;

public class Calculadora {
    public int somar(int val1,int val2){
        return val1 + val2;
    }
    public double somar(double val1,double val2,double val3){
        return val1 + val2;
    }

    public int multiplicar(int val1,int val2){
        return val1 * val2;
    }
    public double multipicar(double val1,double val2){
        return val1 * val2;
    }
    public int subtracao(int val1,int val2){
        return val1 - val2;
    }
    public double subtracao(double val1,double val2){
        return val1  - val2;
    }
    public int dividir(int val1,int val2){
        return val1 / val2;
    }
    public double dividir (double val1,double val2){
        return val1 / val2;
    }

}
